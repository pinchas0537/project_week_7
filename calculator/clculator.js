export function add(num1,num2){
    return num1 + num2
}
export function sub(num1,num2){
    return num1 - num2
}
export function div(num1,num2){
    return num1 / num2
}
export function mul(num1,num2){
    return num1 * num2
}
export function pawer(num1,num2 = 2){
    return num1 ** num2
}
export function root(num1,num2 = 2){
    return num1 ** (1/num2)
}